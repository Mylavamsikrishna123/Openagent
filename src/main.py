# =============================================================================
# PYDANTIC MODELS FOR LLM RESPONSE PARSING
# =============================================================================


class PlanStep(BaseModel):
    """Represents a single step in the agent's execution plan."""

    step_number: int = Field(..., description="The order of this step (1-indexed)")
    action: str = Field(
        ...,
        description="The action to perform: 'navigate', 'click', 'type', 'scroll', 'wait', 'extract'",
    )
    description: str = Field(..., description="Human-readable description of what this step does")
    target: Optional[str] = Field(
        None, description="Target URL for navigate, or selector for click/type"
    )
    value: Optional[str] = Field(None, description="Text value for type actions")


class PlanResponse(BaseModel):
    """Structured response from Groq for task planning."""

    task: str = Field(..., description="The original user task")
    steps: List[PlanStep] = Field(..., description="Ordered list of steps to execute")
    reasoning: str = Field(..., description="Explanation of the planned approach")


class UIElement(BaseModel):
    """Represents a UI element identified by Gemini Vision."""

    element_id: str = Field(..., description="Unique identifier for this element")
    description: str = Field(..., description="What this element is (button, input, link, etc.)")
    text_content: Optional[str] = Field(None, description="Visible text on the element")
    bounding_box: Optional[List[int]] = Field(
        None, description="[x, y, width, height] coordinates"
    )
    is_interactable: bool = Field(..., description="Whether this element can be clicked/typed")
    confidence: float = Field(..., ge=0.0, le=1.0, description="Confidence score 0-1")


class VisionResponse(BaseModel):
    """Structured response from Gemini Vision for screenshot analysis."""

    page_description: str = Field(..., description="Summary of what's visible on the page")
    elements: List[UIElement] = Field(..., description="List of interactable elements found")
    recommended_action: Optional[str] = Field(
        None, description="Suggested next action based on visual analysis"
    )
    relevant_element_ids: List[str] = Field(
        default_factory=list, description="IDs of elements relevant to current task"
    )


class ExtractionResult(BaseModel):
    """Structured data extracted from a webpage."""

    content: str = Field(..., description="Extracted content")
    metadata: dict = Field(default_factory=dict, description="Additional metadata")


# =============================================================================
# LANGGRAPH STATE DEFINITIONS
# =============================================================================


class AgentState(TypedDict, total=False):
    """
    State container for the browser agent workflow.
    
    Uses total=False to allow optional fields that can be added/updated
    by different nodes in the graph.
    """

    # Input
    user_task: str
    """The original task requested by the user"""

    # Planning
    plan: Optional[PlanResponse]
    """The execution plan generated by Groq"""

    current_step_index: int
    """Index of the current step being executed (0-indexed)"""

    # Browser State
    current_url: Optional[str]
    """The current URL the browser is on"""

    screenshot_base64: Optional[str]
    """Base64-encoded screenshot of the current page"""

    # Vision Analysis
    vision_analysis: Optional[VisionResponse]
    """Results from Gemini Vision analysis of the screenshot"""

    selected_element: Optional[UIElement]
    """The specific element selected for interaction"""

    # Execution Results
    step_results: List[dict]
    """Results from each executed step"""

    extraction: Optional[ExtractionResult]
    """Any data extracted from the page"""

    # Control Flow
    error: Optional[str]
    """Error message if something went wrong"""

    is_complete: bool
    """Whether the task has been completed"""

    final_answer: Optional[str]
    """The final result/answer to return to the user"""

    # Retry mechanism for self-healing
    retry_count: int
    """Number of consecutive retries attempted"""

    # Current action from vision (for executor to use)
    current_action: Optional[dict]
    """The current action plan from Gemini Vision"""


